<h1>Строительно-монтажные работы</h1>

<p>Лицензии на право выполнения строительно-монтажных, проектных работ и на выполнение функций заказчика-застройщика,  согласно приказу министерства архитектуры, строительства и ЖКХ РФ № 131 от 03.06.1992 г. «О порядке выдачи разрешений на выполнение строительно-монтажных работ» дают нам право выполнять <b>строительно-монтажные работы</b>.</p>

<p>Разрешения на выполнение строительно-монтажных работ подразделяются на два вида: разрешение на выполнение всех видов строительно-монтажных работ; разрешение на выполнение отдельных видов работ - подготовительные работы, земляные работы, работы по прокладке коммуникаций, устройству фундаментов и т.д. Разрешения выдаются заказчику (застройщику) на объекты производственного и непроизводственного назначения на срок, необходимый для строительства объекта или выполнение отдельных видов работ. При передаче права на строительство объекта другому инвестору, изменении организационно-правовой формы  предприятия-заказчика или передаче подряда на строительство другой организации, разрешение подлежит перерегистрации в инспекции Госархстройндзора в трехнедельный срок.</p>
<br>
<i>Перечень строительно-монтажных работ</i>:

<ul>
  <li>Строительство зданий и сооружений II уровня ответственности</li>
  <li>Общестроительные работы</li>
  <li>Каменные работы</li>
  <li>Монтаж деревянных конструкций</li>
  <li>Монтаж легких ограждающих конструкций</li>
  <li>Оконные и дверные блоки, пространственные конструкции из алюминиевого профиля, профиля ПВХ, стеклопластика, иных полимерных и комбинированных материалов.</li>
  <li>Кровельные работы</li>
  <li>Осуществление функций генерального подрядчика</li>
  <li>Отделочные работы</li>
  <li>Специальные работы</li>
  <li>Монтаж стальных конструкций</li>
  <li>Монтаж конструкций из ПВХ и алюминия.</li>
  <li>Монтаж железобетонных конструкций.</li>
  <li>Защита конструкций, технологического оборудования и трубопроводов</li>
  <li>Защита деревянных конструкций от гниения (устройство антисептических покрытий)</li>
  <li>Герметизация входов и вводов инженерных коммуникаций в сооружения</li>
  <li>Акустическая защита от эксплуатационных шумов и вибраций</li>
  <li>Теплоизоляция трубопроводов и оборудования, работающих при температуре не более 115<sup>о</sup>С</li>
  <li>Подготовка строительной площадки (вырубка деревьев, снос строений, устройство временных дорог).</li>
  <li>Разработка ППР на строительно-монтажные работы.</li>
  <li>Земляные работы (укрепление грунтов, устройство малых архитектурных форм из камня, устройство дренажей).</li>
  <li>Свайные работы, работы по обустройству шпунтового ограждения.</li>
  <li>Контроль качества строительно-монтажных узлов и параметров объекта, включая геодезический контроль элементов.</li>
  <li>Геодезические работы по разбивке внутриплощадочных сооружений и созданию внутренней разбивочной сети сооружений.</li>
  <li>Проектный, технологический и строительный инжиниринг, включая: разработку ТЗ; подготовку и оформление ИРД; технический надзор за СМР; сдачу объекта в эксплуатацию.</li>
  <li>Прочие строительно-монтажные работы.</li>
</ul>
