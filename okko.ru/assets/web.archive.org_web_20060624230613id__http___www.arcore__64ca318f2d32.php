          <td valign="top">&nbsp;<b>Технико маркетинговый анализ</b><br><br>
1. <b>Оценка земельного участка</b><br>
•	Описание земельного участка.<br>
•	Оценка качества земельного участка и его окружения.<br>
•	Выводы о возможности и целесообразности ведения строительства, максимально возможном классе застройки и ценовом сегменте.<br><br>
2. <b>Анализ рынка</b><br>
•	Общая ситуация на рынке и её анализ.<br>
•	Факторы, влияющие на ситуацию на рынке и их анализ.<br>
•	Предложение. Анализ объема и структуры. Количественные и качественные характеристики.<br>
•	Спрос. Анализ объема и структуры. Количественные и качественные характеристики.<br>
•	Динамика заявляемых цен и цен сделок в различных сегментах.<br>
•	Комплексная оценка ценовых сегментов по насыщенности и удовлетворенности спроса по цене и качеству, динамика и перспективы развития.<br>
•	Выводы о возможностях и перспективах работы в каждом сегменте.<br>
•	Тенденции и перспективы развития рынка.<br><br>
3. <b>Концепция освоения участка</b><br>
•	Возможные варианты освоения участка.<br>
•	SWOT-анализ.<br>
•	Выбор наиболее эффективного варианта освоения. Маркетинговое и экономическое обоснование.<br>
•	Концепция строительства и целевой сегмент рынка.<br>
•	Планировка территории.<br><br>
4. <b>Маркетинг</b><br>
•	Мониторинг предложений в районе расположения участка.<br>
•	Анализ конкурентной среды.<br>
•	Маркетинговая стратегия.<br>
•	Ценообразование.<br>
•	Условия реализации.<br>
•	Методы конкурентной борьбы.<br>
•	Определение целевой аудитории.<br>
•	Определение потребностей и предпочтений целевой аудитории.<br>
•	Организация продаж.<br>
•	Бюджет рекламной кампании.<br><br>
&nbsp;<b>Финансовый анализ</b><br><br>
1. <b>Финансовый план проекта</b><br>
•	Разработка прогнозных сценариев реализации объекта на рынке.<br>
•	Рекомендации по достижению максимальной валовой выручки от продаж.<br>
•	Расчет доходов проекта исходя из наиболее вероятного сценария продаж.<br>
•	Расчет инвестиционных издержек и бюджета проекта.<br>
•	Отчет о движении денежных средств (Cash Flow проекта).<br><br>
2. <b>Оценка эффективности проекта</b><br>
•	Определение прибыли проекта и потребности в инвестициях.<br>
•	Расчет интегральных финансовых показателей эффективности проекта:<br>
•	PBР (Период окупаемости)<br>
•	NPV (Чистая приведенная стоимость)<br>
•	IRR (Внутренняя норма рентабельности)<br>
•	Выводы об инвестиционной привлекательности и устойчивости проекта<br><br>
3. <b>Риски проекта</b><br>
•	Анализ чувствительности проекта к различным внешним и внутренним факторам.<br>
•	Определение наиболее значимых факторов.<br>
•	Определение допустимых отклонений значений факторов.<br>
•	Экспертная оценка отдельных рисков и совокупного уровня риска проекта.<br>
•	Меры по снижению уровня рисков проекта.<br>
•	Выводы о приемлемости совокупного уровня риска и надежности проекта.<br>
