<h1>
	Привлекательность мансардных окон.</h1>
<p>
	Для изготовления оконного блока мансардного окна отдают предпочтение высококачественному клееному брусу, который изготавливается из древесины северной сосны. Долгий срок эксплуатации обеспечивается благодаря использованию антисептических пропиток и защитного лакового покрытия, которое выполняет и декоративные функции.</p>
<p>
	Если для помещения характерна повышенная влажность, то хорошо подойдет установка влагостойких полиуретановых стеклопакетов, в основу которых входит деревянный профиль, например, окна VELUXGGUили FAKROFTP-W. Параллельно с мансардными деревянными окнами выпускаются качественные аналоги из пластика. Одно из лидирующих мест по изготовлению пластиковых оконных конструкций для крыш занимает компания ROTO. Материалом для таких окон служит системный профиль ПВХ, который зарекомендовал себя высокой теплосберегающей характеристикой, прочностью, надежностью. Окна из ПВХ-профиля отличаются продолжительным сроком эксплуатации, гарантия на который длится 40 лет. Чтобы избежать теплопотерь, возможных через примыкание к раме створки окна, в мансардные окна монтируются системы уплотнения и контруплотнения. В дополнение на окна ROTOкрепятся специальные алюминиевые накладки особой конструкции. Кроме того теплозащита пластикового окна может усиливаться за счет использования утепляющего блока, который выполнен из эластичного и очень прочного вспененного полипропилена.</p>
<p>
	Обращая внимание на полную герметичность, не следует забывать, что помещению должна быть обеспечена вентиляция. Чтобы избежать дальнейших проблем, связанных с вентиляцией, в мансардных окнах монтируются особые устройства, отвечающие за непрерывное или регулируемое поступление предварительно очищенного воздуха. В окнах VELUXустанавливаются специальные вентиляционные клапаны-форточки со съемным фильтром. Эти форточки гарантируют регулярный воздухообмен до 39 м3/ч. В большинстве моделей мансардных окон компании FAKROесть антиконденсатные устройства для вентиляции в любую погоду. Этот клапан заключает в себе лабиринт, в котором идет смешивание теплого воздуха помещения и холодного воздуха улицы, что является гарантией отсутствия конденсата на внутренней стенке стеклопакета.</p>
<p>
	В случае использования мансардных окон нужно исключить все возможные неудобства, которые могут возникнуть при их использовании. Также следует учесть, что в большинстве случаев мансардные окна могут служить аварийным выходом. Современные окна имеют несколько вариантов открывания. Среди них стандартом является среднеповоротное открывание. Оно обеспечивает поворот створки по центральной оси. Это можно наблюдать в моделях: окна VELUX, модели GGL, GZL1054; окна FAKRO, модели FTP-V, FTS-V, FTL-V; окна ROTO, модели серии 4. Если в окнах установлена комбинированная система открывания, это значит, что можно пользоваться двумя режимами движения створок &ndash; среднеповоротным и подвесным.&nbsp; Среднеповоротным называется вращение створки по центральной оси, а подвесным &ndash; ее подъем, когда верхняя грань окна зафиксирована. К моделям с комбинированной системой открывания относятся окна компании VELUX, модель GPL; окна компании FAKRO, модели FPP preSelect, FE, FK. В моделях люкс-класса компании ROTO седьмой серии у створок есть две оси поворота. Одна из осей расположена в районе верхнего края рамы, а вторая перенесена на три четверти по высоте окна. Режимы открывания переключаются при помощи поворота ручки окна. Такое окно рассчитано на четыре точки запирания.</p>



