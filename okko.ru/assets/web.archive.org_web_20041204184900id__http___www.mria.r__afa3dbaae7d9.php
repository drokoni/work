<br><br><b>Индивидуальное обучение на свой вариант продажи квартиры</b>
<br><br>Данное индивидуальное обучение <b>Агентство недвижимости "МРИА"</b> проводит с собственниками квартир, желающими самостоятельно продать принадлежащую им недвижимость.
<br><br><b>Цель:</b> Сэкономить на комиссионном вознаграждении  риэлторских фирм, которое составляет в среднем 7-10% от рыночной стоимости квартиры заплатив при этом всего 500 долларов за обучение на свой вариант.

<br><br><b>Индивидуальное обучение делится на разделы:</b>
<br><br>1. Состав правоустанавливающих документов на вашу квартиру, необходимых для ее отчуждения;
<br>2. Оценка стоимости вашей квартиры;
<br>3. Определения мест и способов рекламы, разработка рекламных материалов;
<br>4. Обучение методом показов вашей квартиры потенциальным покупателям;
<br>5. Технология внесения аванса, задатка, составление договора;
<br>6. Составление договора Купли-продажи в простой письменной форме;
<br>7. Состав пакета для государственной регистрации договора отчуждения;
<br>8. Организация безопасных взаиморасчетов по договору отчуждения.
<br><br>Стоимость обучения по индивидуальной программе составляет 500 долларов США.
<br><br><b>Выгода:</b>
	<br><br>1. Большинство Агентств недвижимости определяют стоимость услуг по продаже недвижимости от ее рыночной стоимости, что составляет от 7-10 %, поэтому на продаже квартиры после индивидуального обучения  стоимость всего 500 долларов вы сэкономите огромные деньги, которые Вам пригодятся. 
<br>2. В случае  если вам не удалось продать вашу квартиру самостоятельно вы можете воспользоваться нашими услугами. <b>Агентство недвижимости "МРИА"</b>  окажет профессиональные услуги:  проведение эффективной рекламной кампании, выгодную продажу квартиры, осуществит подбор с учетом ваших требований варианта для покупки квартиры, юридическую экспертизу документов, сбор и подготовку необходимых документов для проведения сделки любой сложности, регистрацию прав собственности, а также обеспечит безопасность проведения расчетов. 



<a name = 3><br><b>Продажа квартиры</b>
<br>Для того чтобы продать принадлежащую Вам квартиру в Москве или Подмосковье необходимо:

<br><br><b>Оценить квартиру.</b> Цена квартиры зависит от того, в каком районе она расположена, от ее удаленности от метро, типа дома, этажа, статуса квартиры (свободная, приватизированная и т.д.), от площади квартиры, ее состояния (евроремонт, косметический ремонт, требует ремонта), наличия телефона, лифта и др. факторов. Для оценки квартиры необходимо обратиться к профессиональным риэлтерам, которые располагают обширными базами данных о совершившихся сделках купли-продажи квартир. Риэлтеры смогут точно определить рыночную стоимость Вашей квартиры методом сравнительного анализа с реальными ценами уже проданных квартир, что позволит избежать необоснованного завышения или занижения стоимости квартиры и выдадут Вам оценочный сертификат.

<br><br><b>Найти покупателя на квартиру.</b> представитель агентства выезжает к Вам, составляет подробное описание Вашей квартиры, размещает рекламу Вашей квартиры в печатных изданиях и риэлтерских компаниях. Также возможен поиск альтернативного жилья для Вас. Агент проведет предварительные переговоры с потенциальными покупателями, организует просмотр квартиры. 

<br><br><b>Подготовить необходимый пакет документов для сделки и проект договора купли-продажи недвижимости.</b> Даже если Вы самостоятельно нашли покупателя Вашей квартиры, подготовку документов необходимо доверить профессионалам, которые квалифицированно подготовят все необходимые документы и оформят договор купли-продажи. 

<br><br><b>Произвести взаиморасчеты с покупателем. </b>Взаиморасчеты обычно осуществляются с использованием абонированной банковской ячейки, также могут применяться и другие формы взаиморасчетов.

<br><br><b>Оформить сделку купли-продажи недвижимости. </b>Зарегистрировать договор купли-продажи недвижимости в отделении Мосрегистрации и осуществить передачу недвижимости от продавца к покупателю.
<br>Сделка с участием или без участия агентства недвижимости проводится практически по одной схеме. Поэтому давайте начнем сначала.
<br>Допустим, у вас есть квартира, которую вы решили продать. Основная масса сделок на рынке недвижимости - примерно 80% - это так называемые альтернативные, продажа своей квартиры для покупки другой. Но сегодня мы будем говорить только о продаже жилья с целью получения денег, так как в любую комбинированную сделку эта процедура включена.

<br><br><b>Оценка возможностей, консультации</b>
<br>Первое, с чего нужно начинать при продаже квартиры, - хотя бы примерно оценить собственность, то есть получить информацию о стоимости. Самый простой способ - изучение московских риэлторских изданий, оперативно подающих подобную информацию, например, газета "Из рук в руки". Обязательно имейте в виду, что цены на квартиры, проставленные в газете, - не факт, а пожелания продавцов.

<br><br><b>Реклама</b>
<br>Следующий немаловажный момент - подача квартиры на рынок. Нужно сделать так, чтобы о ней узнало как можно больше потенциальных покупателей. На сегодняшний день в Москве существует порядка тысячи мелких и средних агентств недвижимости, где информацию о вашей квартире бесплатно примут, внесут в базу и будут предлагать всем потенциальным покупателям. Крупные агентства недвижимости такую информацию не принимают, предпочитая заключение эксклюзивного договора, который предполагает перекладывание всех забот по рекламе, оформлению и сбору пакета необходимых документов на свою фирму.
<br>Еще один способ - подача объявления в газету "Из рук в руки" или в Internet с указанием вашего контактного телефона (как правило, домашнего). Здесь есть свои плюсы и минусы. Указывая домашний телефон, вы имеете прямой контакт с покупателем или агентством недвижимости. Но имейте в виду, что, общаясь с этими людьми напрямую, нельзя предугадать, насколько порядочен тот или иной позвонивший и каковы его настоящие намерения.
<br>Многое зависит от регулярности подачи объявлений. Если квартира выставлена по ликвидной цене, объявление должно выходить не менее двух раз в неделю. Если квартиру нужно продать очень быстро - три раза в неделю. Подавать объявление каждый день бессмысленно.

<br><br><b>Показ, торг</b>
<br>Начинаются ответные звонки, вы проводите переговоры, общаетесь, согласовываете время просмотров. К этому надо относиться очень аккуратно. Вы не знаете людей, заходящих в вашу квартиру, поэтому ценные вещи и предметы с видных мест лучше убрать. Но все равно придется показывать все "закоулки" (например, есть ли обои за шкафом). Опыт показывает, что потенциального покупателя интересует только состояние комнат и высота потолка. Он может рассматривать комнаты больше пяти минут только в том случае, если предполагается продажа квартиры с мебелью или перепланировка. Если же такой договоренности нет, а интерес к мебели возник - это ненормально и должно вас насторожить.
<br>Допустим, человек согласился купить квартиру и готов обсуждать условия ее продажи. Начинается торг. Есть специальное выражение - выдержать торг. Согласившись сразу на условия покупателя, вы покажете, что можете двигаться в цене и дальше, чем воспользуются незамедлительно. Это элементарная психология, и так происходит часто.
<br>Совет: первому клиенту, решающему немедленно купить вашу квартиру по первой же предлагаемой цене, мы рекомендуем ее… не продавать. Есть основание предположить, что вы недооценили квартиру и продаете дешево. Хотя, может быть, вам просто повезло, и это именно тот человек, которому нужна квартира именно в этом месте, именно за эту цену. Такое тоже случается.
<br>Если на просмотр покупатель пришел с представителем какого-нибудь агентства недвижимости - у вас есть шанс получить часть услуг практически бесплатно. Агент может попытаться решить Ваши проблемы в пределах одного агентства недвижимости, таким образом, вольно или невольно, вы попадаете на обслуживание определенной фирмы, которая может… выставить счет за это самое обслуживание. Например, сказать, что "...ситуация более чем серьезная, мы беремся ее решить, и стоит это столько-то". Вы вправе принимать или не принимать предлагаемые условия, можете отказаться от услуг и заняться продажей самолично.
<br>Еще один совет: если квартира продается без участия агентства, продавцам лучше проводить переговоры у себя дома или в офисе, покупателям - у себя. В таких случаях присутствует элемент "своей" территории, где человек чувствует себя уютнее, спокойнее, уверенней.

<br><br><b>Договоренность с продавцом (покупателем)</b>
<br>Многие продавцы после вроде бы положительного решения, высказанного на словах, снимают рекламу квартиры, не предполагая, что покупатель может продолжать поиски более выгодного варианта. Поэтому, достигнув договоренности, продавцы должны ставить вопрос о некоторой предоплате в обеспечение обязательства купить вашу квартиру.
<br>Покупателям же мы советуем этот вопрос не ставить, уйти от него, чтобы быть свободными в своих дальнейших действиях. Правда, в этом случае есть риск понравившуюся квартиру "упустить", если продавец найдет более выгодного клиента.
<br>Дальше следует довольно щекотливый момент. Допустим, вы договорились, что покупатель дает какое-то количество денег в обеспечение приобретения вашей квартиры. Как провести это, как передать деньги, чтобы в дальнейшем не возникло проблем?
<br>Существуют юридические понятия: соглашение о задатке и авансовое соглашение. Если вы, как продавец квартиры, подписали соглашение о задатке, то должны понимать, что расторжение договора о намерениях, то есть отказ от продажи квартиры этому покупателю, может повлечь определенные санкции. Поскольку, с юридической точки зрения, это читается как "невыполнение обязательств продавца и возврат суммы в двойном размере". Таким образом, опытный и грамотный покупатель со своим юристом легко поставят вас в финансовую зависимость.
<br>Для покупателя квартиры, кроме адреса, ценовых характеристик и сроков продажи, нужно не забыть оговорить сроки юридического (выписка) и физического освобождения квартиры. Физическое освобождение - это вывоз из нее вещей и мебели. Если сроки не оговорены, покупатель часто настаивает на освобождении квартиры сразу после подписания нотариального договора.

<br><br><b>Проверка, подготовка сделки, сбор документов</b>
<br>После того как составлены все бумаги и принята предоплата за квартиру, начинается процедура подготовки сделки. На продавцов возлагается обязанность собрать полный комплект документов. Это означает необходимость пробежать по всем инстанциям и добиться, - подчеркиваю, в определенные сроки - получения нужных бумаг. Это довольно сложная процедура.

<br><br><b>Для продажи квартиры необходимо иметь на руках следующие документы:</b>
<br>1. Правоустанавливающие документы, в зависимости от способа приобретения квартиры, например: Договор передачи, Свидетельство о собственности на жилище, Справка ЖСК о выплаченном пае, регистрационное удостоверение БТИ, Свидетельство о праве на наследство, договоры купли-продажи, дарения, мены, ренты, решение суда. Если документы на квартиру утеряны, то жилье можно продать по Выписке из реестра, которую выдает Комитет по регистрации. Правда, в этом случае надо иметь в виду, что теоретически выписку можно брать хоть каждый день. То есть возможна ситуация, когда продавец, приняв у вас предоплату, завтра продаст свою квартиру другому покупателю. Поэтому в таком случае в качестве предоплаты мы советуем давать не больше $100-200, чтобы свести свои потери к минимуму.
<br>2. В БТИ необходимо получить: справку (форма 11-А, действительна 1 месяц); поэтажный план, экспликацию (действительны 1 год).
Чтобы собрать эти бумаги, потребуются оригиналы самих правоустанавливающих документов, ваш паспорт и около 150 руб.
<br>3. В РЭУ нужно взять:
<br>- выписку из домовой книги - в паспортном столе;
<br>- копию финансово-лицевого счета и справку об отсутствии задолженности по квартплате - в бухгалтерии. 
<br>Все эти справки действительны 1 месяц. Чтобы их собрать, вам потребуются книжка по квартплате, паспорт или доверенность (если вы не собственник).
