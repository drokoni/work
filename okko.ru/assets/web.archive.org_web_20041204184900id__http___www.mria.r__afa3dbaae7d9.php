<br>После того как все вышеназванные документы будут собраны, нужно обязательно их сверить на соответствие фамилий, адресов, дат, подписей и их расшифровок, размеров всех фигурирующих площадей. На сегодняшний день мы насчитали порядка 20-22 видов правоустанавливающих документов на квартиру, которые только могут быть. Подчеркиваю - не 2, не 5, не 8, а 22! Поэтому мы настоятельно рекомендуем на этом этапе получить юридическую консультацию. Покупателям квартиры - чтобы понять, насколько эти документы соответствуют требованиям закона. Продавцу - чтобы быть уверенным, что по ним возможно продать квартиру в указанные сроки. Часто складывается ситуация, когда продавец, считая, что имеет на руках достаточные для сделки документы, берет задаток под четко оговоренные сроки оформления, а выдержать их не может. Вдруг выясняется, что кто-то из собственников умер, а наследство не открывалось, потому что люди думали, что это не нужно. В результате продать такую квартиру нельзя до тех пор, пока не утрясешь все юридические формальности.

<br><br><b>Банк, нотариус, регистрация</b>
Но, предположим, все документы на квартиру в порядке и наступает время обсудить вопросы по взаиморасчетам, где и как их производить. Самый распространенный способ - расчет через банковскую ячейку под зарегистрированный на имя покупателя договор купли-продажи квартиры. Советуем вам очень внимательно подойти к выбору банка. Если покупатель предлагает конкретный банк, мотивируя тем, что там хранятся его деньги, а председатель правления его лучший друг, мы рекомендуем на этот вариант не соглашаться. В этой ситуации вполне допустимы всякого рода злоупотребления клиентским положением. Должна быть нейтральная территория, которая устроит обе стороны. Поэтому вопрос по взаиморасчетам нужно обязательно проговаривать заранее, еще в момент передачи (или получения) задатка.
<br>После закладки денег вам предстоит нотариальное (по вашему желанию) оформление договоров или сразу сдача документов на государственную регистрацию.

<br><br><b>Юридическое и физическое освобождение, акт приема-передачи</b>
