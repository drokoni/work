<h1>Дизайн интерьера</h1>
<h2>Дизайн интерьера квартир</h2>
<p>При создании <b>дизайна интерьера квартиры</b> учитываются индивидуальные потребности ее владельца. Все ваши пожелания относительно дизайна квартиры найдут блестящую реализацию, если вы обратитесь в Арт-Дизайн-Профи.</p>

<p>В разделе нашего сайта "Наши работы" вы можете посмотреть дизайны некоторых квартир, которых мы делали. Получившиеся интерьеры квартир не разочаровали наших заказчиков, потому что они считают, что серьезные люди доверяют <i>дизайн интерьера</i> только профессионалам, то есть нам :)</p>

<p>Разработка концепции дизайна интерьера, создание чертежей "от руки" и компьютерное моделирование. Осуществление подбора элементов декора, отделочных материалов, мебели, светильников, штор. Ведение проекта, контроль за исполнением (авторский надзор) и шеф-монтаж.</p>

<p>Грамотный дизайн интерьера квартиры приводит к тому, что в квартире приятно находиться вам и вашим гостям. Интерьер должен быть таким, чтобы максимально эффективно использовать внутреннее пространство квартиры, чтобы было стильно, но при этом свободно и гармонично. Приходя домой после напряженного дня, вы будете чувствовать единение элементов интерьера квартиры с вашим настроением и внутренним миром, что даст вам приятный настрой на остаток вечера. А друзья и знакомые, зайдя к вам в гости, будут удивлены индивидуальности и дизайну вашей квартиры.</p>

<p>Для комфортного нахождения в квартире недостаточно эргономичного дизайна, функциональность тоже должна не уступать в продуманности. Все предметы, которые могут вам понадобиться, должны быть под рукой в нужный момент. Техническое оснащение встроенной техникой может быть полезно. Несущие балки обычно скрываются и незаметно вписываются в дизайн интерьера, например, используются многоуровневые потолки или арки и так далее. Выдерживание стиля квартиры в целом и отдельных комнат, практичность расположения розеток, выключателей, дверей.  Квалифицированные сотрудники Арт-Дизайн-Профи имеют богатый опыт и умеют сочетать вышеприведенные показатели качества дизайна квартир. О чем свидетельствуют исключительно положительные отзывы наших клиентов.</p>

