 <TABLE cellSpacing=1 cellPadding=1 width=500 align=center border=0>
        <TBODY>
        <TR vAlign=top>
          <TD colSpan=2>
          <P align=justify>
<b>Блок-контейнер </b>- отдельно стоящий модуль, предназначенный для 
            размещения административно-бытовых, жилых и служебных помещений при 
            организации вахтовых поселков, мобильных исследовательских 
            комплексов, баз поисково-спасательных служб, туристических баз и 
            т.п. </P></TD></TR>
        <TR vAlign=top>
          <TD colSpan=2>
          <P align=justify>

<a href="http://www.insi.ru/cgi/content.cgi?folder=26&sh=76"><IMG height=200
          src="http://www.insi.ru/photo/pr10.jpg" width=500 
           alt="Блок-контейнер " Vspace="10" hspace="10"></a> 

<B>В блок-контейнере</B> можно разместить гостиницу, общежитие, 
            служебные помещения, госпиталь, столовую, узел связи, клуб и т.п. 
            Особенно удобно использовать здания из блок-контейнеров в 
            местах, удаленных от населенных пунктов и труднодоступных для 
            доставки строительных материалов и техники. </P></TD></TR>
        <TR vAlign=top>
          <TD colSpan=2>
         <P align=justify>
<B>Конструкция 
            блок-контейнера</B> имеет каркасно-панельное решение. Панели стен, пола и потолка блок-контейнера собираются в 
            жесткий блок при помощи электросварки. Каркас панелей - из 
            металлических гнутых профилей и деревянных брусков, который 
            заполняется минераловатным утеплителем и снаружи обшивается 
            металлическим профилированным листом. Внутренняя обшивка панелей 
            стен и потолка - плиты ДВП или вагонка. При отделке внутренних 
            помещений могут быть использованы любые другие материалы. По 
            согласованию с заказчиком и в зависимости от функциональных свойств 
            помещений блок-контейнер комплектуется мебелью.</P></TD></TR>
        <TR vAlign=top>
          <TD colSpan=2>
         
<P align=justify><b>Конструктивные особенности блок-контейнеров</B> позволяют 
            производить перепланировку, изготавливать контейнер без одной или 
            двух наружных стен для объединения в модули, а также устанавливать 
            их в два уровня, устраивая общие наружные площади и 
        лестницы.</P></TD></TR>
        <TR vAlign=top>
          <TD colSpan=2>
       <P align=justify>
<B>Блок-контейнеры обычно имеют размеры,</B> 
            превышающие транспортные габариты. Поэтому транспортировка блок-контейнеров осуществляется 
            спецтранспортом по предварительному согласованию с ГИБДД.</P></TD></TR>
        <TR vAlign=top>
          <TD colSpan=2>
        <P align=justify>
<B>Блок-контейнеры</B> обладают неоспоримыми достоинствами: 
            полная заводская готовность, мобильность, единый транспортный 
            габарит, быстрый монтаж и пуск в эксплуатацию.</P></TD></TR>
        <TR vAlign=top>
          <TD colSpan=2>                     




<P align=justify>

    
  <i>
<b>Завод ИНСИ</b> является производителем современных блок-контейнеров, а также материалов для строительства: металлочерепицы, металлосайдинга, профнастила, термопрофиля,
вентилируемых фасадов, водосливных систем. 

 <P align=justify>Вся выпускаемая продукция представляет единый комплекс изделий,
составляющих Архитектурно - строительную систему ИНСИ. 
 <P align=justify>
Все строительные материалы являются экологически чистыми, отвечают
мировым стандартам и имеют соответствующие российские и зарубежные
сертификаты, а также заводскую гарантию качества.
 <P align=justify>
Завод ИНСИ имеет 16 филиалов в России и СНГ. <A href="http://www.insi.ru/optima/map.php" style="color: #666666">Дополнительные материалы</a> о нашей продукции. 

</i>

