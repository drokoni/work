  <TD ROWSPAN=6 width=592 height=100% class=main valign=top><h3>Мягкая кровля</h3>
<br>
<img src="images/flexroof/flex.jpg">
<br><br>
<img src="images/flexroof/katepalKL.jpg">
<img src="images/flexroof/katepalKLcolor.jpg">
<br><br>
<b>KL</b> - классическая коллекция одноцветной гибкой черепицы шестигранной формы. Шестигранные черепицы Katepal столь же технологически совершенны, как и прямоугольные. Чистота и ясность цвета делают эту коллекцию идеальным выбором тех, кто ценит строгие архитектурные решения.
<br><br>
<b>Зеленый и коричневый</b> - максимально естественные, природные цвета.<br>
<b>Красный</b> - сочетание яркости и гармоничности.<br>
<b>Серый и черный</b> - классическая строгость и благородство.<br>
<br><br><br>
<img src="images/flexroof/katepalJazzy.jpg">
<img src="images/flexroof/katepalJazzycolor.jpg">
<br><br>
Черепицы типа <b>Jazzy</b> - благодаря смешению черных и окрашенных гранул создают на кровле причудливый, переливающийся рисунок.
<br><br>
<b>Красный</b> - яркий, но при этом уютный цвет, напомнит Вам о тихом вечере возле камина.<br>
<b>Серый</b> - спокойный, но не лишенный оригинальности цвет. Прекрасно подчеркнет элегантность архитектурного решения Вашего дома.<br>
<b>Коричневый</b> - гармония природы на Вашей кровле.<br>
<b>Зеленый</b> - теплый, домашний цвет.<br>
<br><br><br>
<img src="images/flexroof/ruflexKatrilli.jpg">
<img src="images/flexroof/ruflexKatrillicolor.jpg">
<br><br>
"Тени" в верхней части черепиц придают кровле объемный, рельефный вид, а удивительно натуральные природные расцветки позволяют воплотить самые разнообразные архитектурные проекты. Сами названия цветов коллекции <b>RUFLEX Katrilli</b> говорят о стремлении к максимальной гармонии с природой.
<br><br>
<b>Осенний красный</b> - неповторимое очарование осеннего леса.<br>
<b>Кора дерева</b> - теплота и величие столетних финских сосен.<br>
<b>Синий</b> - чистота и ясность летнего неба.<br>
<b>Зелень моховая</b> - сочная зелень лесных холмов.<br>
<b>Серый</b> - благородные оттенки серебра.
<br><br><br>
<img src="images/flexroof/ruflexSKatrilli.jpg">
<img src="images/flexroof/ruflexSKatrillicolor.jpg">
<br><br>
Коллекция гибкой черепицы премиум-класса <b>RUFLEX SUPER Katrilli</b> кроме высокого уровня технических показателей расширяет цветовую гамму типа <b>Katrilli</b> на 3 оригинальных цвета, отвечающих последним тенденциям кровельной моды.
<br><br>
<b>Лишайник</b> - естественность и красота самой природы.<br>
<b>Иней</b> - легкая дымка ноябрьского утра.<br>
<b>Вереск</b> - строгая красота ярких цветов на темных скалах.<br>
<br><br><br>
<img src="images/flexroof/ruflexSRocky.jpg">
<img src="images/flexroof/ruflexSRockycolor.jpg">
<br><br>
Гибкая черепица премиум-класса <b>RUFLEX SUPER Rocky</b> - разработка дизайнеров <b>KATEPAL OY</b>, открывающая новую эру в дизайне кровель. Прямоугольная форма черепиц придает кровле оригинальный вид, напоминающий старинную гонтовую кровлю, а шесть цветовых вариантов предоставляют самые широкие возможности для создания эстетичных и надежных кровель.
<br><br>
<b>Тайга</b> - Вы читали сказку "Волшебник Изумрудного города"? Возможно, его крыши были покрыты именно такой плиткой.<br>
<b>Голубая Лагуна</b> - необычный ярко-синий оттенок Голубой Лагуны выгодно выделит Ваш дом среди всех остальных.<br>
<b>Медный отлив</b> - благородство внешнего вида, налет времени сочетаются в этом оттенке с прочностью и долговечностью.<br>
<b>Гранит</b> - оснговательность и глубокий цвет природного камня.<br>
<b>Балтика</b> - благородный серо-стальной цвет суровых волн Балтики.<br>
<b>Спелый каштан</b> - теплота, уют и спокойствие.<br>
