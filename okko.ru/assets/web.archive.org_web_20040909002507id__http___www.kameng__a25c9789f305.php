<center><img src="images/doors/pict1.jpg"><br>Рисунок 1<br><br><b>Описание конструкции полотна.</b></center>
&nbsp&nbsp&nbsp Полотно двери изготовлено из <b>двух</b> легированных стальных листов, толщиной 1,2мм (модели филенчатого типа) и 2,00 мм (модель "Классик") каждый. Толщина стальных листов оптимально определена с учетом технологии изготовления, а также обеспечения безопасности и надежности дверей в эксплуатации.
<br>
&nbsp&nbsp&nbsp Профиль дизайна наносится путем штампа на пресс-машинах, подбор и нанесение краски на- поверхность отштампованных листов производится на компьютере в специальных покрасочных камерах.
<br>
&nbsp&nbsp&nbsp Полотно имеет стальной притвор (12мм), который повышает надежность конструкции. Он предотвращает возможность повреждения ригелей и отжатие полотна входной двери. По периметру полотна установлен <b>резиновый уплотнитель</b>, который защищает помещение от попадания влаги, пыли и шума.
<br>
&nbsp&nbsp&nbsp Дверное полотно моделей с цельногнутым сложным коробом ("Венеция", "Квадро", "Сандал", "Модерн") имеет закругленные внешние углы радиусом 50 мм, что делает полотно эстетичным и нетравмоопасным.
<br>
&nbsp&nbsp&nbsp Пространство внутри полотна заполнено специальным наполнителем - <b>вермикулитом</b> -принципиально новым материалом, внедренным в конструкцию специалистами корпорации по результатам <b>последних разработок</b>.
<br>
<br>
&nbsp&nbsp&nbsp <b>Вермикулит</b> - природный минерал из группы гидрослюд - высокоэффективный долговечный и нетоксичный теплоизоляционный материал. Тепло- и шумоизоляционные характеристики вермикулита <b>выше</b>, чем у известных и распространенных утеплителей - минеральной ваты или полиуретановой пены. Вермикулит обладает низким объемным весом, малой теплопроводностью, высокой огнестойкостью (t плавления +1350 С). Он не разрушается с течением времени, не засоряет замковую часть, герметично заполняет полость дверного полотна, в отличие от пены или минеральный ваты. <b>Уникальные сочетания физико-механических характеристик</b> вермикулита обеспечивают также <b>дополнительное усиление</b> конструкции двери: вермикулит подается в пространство полотна в расплавленном состоянии, герметично заполняет полость и застывает, приобретая свойства камня.
<br>
&nbsp&nbsp&nbsp В полотно двери встроен <b>звонок</b> (4,5 В) и <b>глазок</b>. <b>Звонок</b> работает автономно, от 3-х "пальчиковых" батареек 1,5 V каждая, модель size AA, не требуя подключения в электросеть. В случае необходимости, замена элементов питания звонка осуществляется просто (с верхнего торца дверного полотна в открытом положении). <b>Глазок - объемный</b> и имеет широкий радиус обзора (около 180°).
<br><br>
<center><b>Типы покрытий.</b></center>
&nbsp&nbsp&nbsp Полотна дверей покрываются различными типами высококачественных покрытий: полихлорвинил, металлолак и полимерная влагостойкая краска.
<br>
&nbsp&nbsp&nbsp <b>Полихлорвинил (PVC)</b> - покрытие, обладающее большой прочностью, устойчивостью к механическим повреждениям, коррозии, атмосферным воздействиям и сохраняющее блеск и цвет в течение длительного времени. Данное покрытие также обладает высокими противопожарными свойствами. Цветовая гамма дверей с покрытием данного вида разнообразна: "махагон", "орех" и "кожа".
<br>
&nbsp&nbsp&nbsp <b>Металлолак</b> - покрытие, представляющее собой двухслойную систему окраски с визуальным эффектом блеска металла. Полотно двери грунтуется и покрывается высококачественной <b>порошковой автокраской</b>, а затем наносится прозрачный лак. В результате создается блестящее, очень <b>прочное лаковое покрытие, стойкое к атмосферным воздействиям</b> (напри мер: дождь, снег, перепады температур и т.д.). Данные свойства позволяют использовать двери с покрытием металлолак <b>для установки в коттеджах, частных домах</b>. В Россию поставляются двери металлолак "коричневый" и металлолак "абрикос".
<br>
&nbsp&nbsp&nbsp <b>Полимерная влагостойкая краска - функциональное декоративное покрытие</b>; соче тает качества хорошего пленочного покрытия с отличными механическими свойствами и прекрасным внешним видом. При изготовлении стальных дверей PANPAN используется полимерная краска следующих цветов: "коричневый" и "черный".
<br><br>
<center><b>Описание конструкции короба и наличника.</b></center>
&nbsp&nbsp&nbsp Короб и наличник изготавливаются из легированной стали толщиной 2,0 мм по двум технологиям: стандартной и цельногнутой.
<br>
&nbsp&nbsp&nbsp При <b>стандартной технологии</b> изготовления короб и наличник имеют сварную конструкцию.
<br>
&nbsp&nbsp&nbsp При <b>цельногнутой технологии</b> короб и наличник штампуются из прямоугольной заготовки. Полученная <b>цельная конструкция сложного короба и наличника не имеет сварных швов</b>. Кроме того, данная конструкция имеет закругленные внутренние углы радиусом 50 мм, которые придают дверному блоку особую <b>герметичность и прочность</b>. Перегнуть или деформировать такую конструкцию практически невозможно. Данная технология изготовления также является уникальной разработкой компании.
<br>
&nbsp&nbsp&nbsp В коробе расположены <b>три скрытых навеса</b>, изготовленные из легированной стали толщиной <b>5,0 мм</b>. Наличие потайных петель предотвращает их срезание с наружной стороны, а также защищает дверь от "проседания" шарниров. Все комплектующие обработаны гальваническими материалами, которые предупреждают коррозию металла.
<br>
&nbsp&nbsp&nbsp Также, как и на дверном полотне, по периметру короба установлен резиновый уплотнитель.
<br><br>
<center><b>Описание замкового механизма и системы безопасности.</b></center>
&nbsp&nbsp&nbsp Система безопасности дверей PANPAN представляет собой 4-х стороннюю систему запирания: замок сейфового типа (укомплектованный внутренней задвижкой) и противосъемные штыри со стороны навесов.
<br>
&nbsp&nbsp&nbsp Стальные двери комплектуются одноцилиндровым замком повышенной секретности, имеющими <b>9-ти ригельную трехстороннюю</b> систему запирания. При закрытии замка ригеля выдвигаются <b>в верхнюю, пороговую и в замковую части рамы</b> на разных уровнях. Данная система запирания исключает возможность силового отжатия дверного полотна.
<br>
&nbsp&nbsp&nbsp Для удобства с внутренней стороны дверь оснащена задвижкой с дополнительным <b>10-м ригелем</b>.
<br>
&nbsp&nbsp&nbsp Дополнительная надежность двери обеспечивается <b>двумя противосъемными штырями</b> диаметром <b>15 мм</b>, предусмотренными в конструкции дверной коробки со стороны скрытых петель.
<br>
&nbsp&nbsp&nbsp Дверь открывается и закрывается ключами с внутренней и внешней стороны.
<br>
&nbsp&nbsp&nbsp Ключи изготавливаются по специальной технологии с применением <b>лазерной перфорации</b>, то есть имеют точечные насечки. Их число равно количеству стопорных штифтов (пинов) в цилиндровом механизме. Подделать такой ключ практически невозможно - ключи выполнены в <b>специальной "полусферической" форме</b>, замки имеют более <b>3 млн.</b> комбинаций ключа. Каждый ключ единственный и неповторимый. В комплекте к замку прилагаются <b>б рабочих</b> (в запаянной оболочке) и <b>2 монтажных</b> ключа (для работы строителей или установщиков).
<br>
&nbsp&nbsp&nbsp Замок оснащен <b>системой автоматической перекодировки</b>. Кодовый механизм цилиндрового замка состоит из корпуса, вращающегося цилиндра и подпружиненных штифтов (пинов). При вставлении ключа в замочную скважину штифты заполняют профиль ключа и занимают позицию, которая позволяет цилиндру поворачиваться внутри корпуса. Для перекодировки необходимо вставить в цилиндр рабочий ключ и повернуть его в замке. При этом активируются дополнительные пины, недоступные для монтажного ключа. <b>После использования рабочего ключа монтажными ключами дверь открыть уже невозможно</b>.
<br>
&nbsp&nbsp&nbsp Замок защищен от механических повреждений <b>бронированной накладкой</b> толщиной 2 мм. Кроме того, в замковую часть с внутренней стороны полотна вмонтирована <b>дополнительная стальная пластина</b>, которая усиливает защиту механизма замка.
