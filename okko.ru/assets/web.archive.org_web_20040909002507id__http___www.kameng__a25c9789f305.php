&nbsp&nbsp&nbsp Одно из главных конкурентных преимуществ продукции PANPAN - широкий ассортиментный ряд дверей эконом- и элит-класса, конструктивные особенности и дизайнерское исполнение которых удивят даже самого искушенного покупателя.
<br><br>
<b>Двери PANPAN выполнены в трех основных стилях и направлениях:</b><br>
<ul>
1)	классический: "Классик";<br>
2)	американский: "Квадро", "Модерн";<br>
3)	европейский: "Венеция", "Сандал".<br>
</ul>
&nbsp&nbsp&nbsp Входная стальная дверь PANPAN поставляется в упаковке в сборе (рисунок 1). Дверной короб (1) и наличник (2) изготовлены из 2-х мм легированной стали. Внутри полотна (3) расположен металлический каркас, состоящий из вертикальных и горизонтальных ребер жесткости, придающих конструкции жесткость. По периметру полотна установлен двойной резиновый уплотнитель (4). В дверное полотно встроен одноцилиндровый 9-ти ригельный сейфовый замок с броненакладкой (5), объемный глазок широко обзора (б) и автономный звонок (7). Усиливают надежность двери 2 скрытых противосъемных штыря (8) и 3 потайных навеса из высоколегированной стали (9). Дверь поставляется в комплекте с фурнитурой, анкерными болтами и фирменными заглушками (б шт.). В комплект поставки каждого изделия входит также технический паспорт с инструкцией по монтажу и эксплуатации.
<br><br>
&nbsp&nbsp&nbsp Стальные двери PANPAN предназначены для установки с внешним открыванием, имеют правое и левое направление открывания и <b>изготавливаются по 5 типоразмерам, соответствующим типовым дверным проемам</b>.
