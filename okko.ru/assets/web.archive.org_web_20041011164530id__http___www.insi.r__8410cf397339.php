<table width=500 border="0" cellspacing="1" cellpadding="1" align="center">
  <tr valign="top"><td colspan=2><p align=justify><b>Модульные здания</b> - универсальные конструкции, основным несущим элементом которых является высокопрочный каркас, предназначенные для размещения жилых и служебных помещений. Фирма ИНСИ спроектирует, изготовит и смонтирует любые модульные здания. Собственное производство позволит изготовить модульные здания любой сложности.</p></td></tr>
  <tr valign="top"><td colspan=2>
<p align=justify>

<img src="http://www.insi.ru/photo/bigpr4.jpg" width="170" height="117" align=left  alt="Модульные здания" Vspace="10" hspace="10">

<b>Модульные здания</b> пользуются большой популярностью в торговле (киоски, павильоны), в строительстве (бытовки). Также модульные здания могут применяться в качестве медицинских пунктов, столовых, душевых, мини-цехов т.п.</p></td></tr>
  <tr valign="top"><td colspan=2><p align=justify><b>Модульные здания</b> благодаря своим конструкционным особенностям, позволяют производить перепланировку, надстраивать второй уровень.</td></tr>
  <tr valign="top"><td colspan=2><p align=justify><b>Модульные здания</b> могут комплектоваться дополнительным оборудованием: системами водоснабжения, теплоснабжения, канализации, кондиционирования. Фирма ИНСИ готова  смонтировать дополнительное оборудование по просьбе заказчика.</p></td></tr>
  <tr valign="top"><td colspan=2><p align=justify><h5>Модульные здания - это новый вид сооружений, обладающий рядом преимуществ:</h5></p></td></tr>
    <tr valign="top"><td colspan=2><p align=justify><ul><li>быстрый монтаж;</li><li>приемлемая стоимость;</li><li>мобильность (незаменимая в случае необходимости изменения места дислокации);</li><li>многофункциональность;</li><li>долговечность;</li><li>возможность многократного применения.</li></ul></p></td></tr>
    <tr valign="top"><td colspan=2><p align=justify><b>Модульные здания</b> могут быть изготовлены по схемам заказчика и комплектоваться дополнительным оборудованием. Стоимость каждого модульного здания рассчитывается индивидуально, в зависимости от комплектации здания инженерными коммуникациями, вида наружной и внутренней отделки, количества внутренних перегородок и площади остекления.</p></td></tr>
   
<tr valign="top"><td colspan=2>
<i>
<b>Завод ИНСИ</b> является производителем современных модульных зданий, а также материалов для строительства: металлочерепицы, металлосайдинга, профнастила, термопрофиля, вентилируемых фасадов. 

 <P align=justify>Вся выпускаемая продукция представляет единый комплекс изделий,
составляющих Архитектурно - строительную систему ИНСИ. 
 <P align=justify>
Все строительные материалы являются экологически чистыми, отвечают
мировым стандартам и имеют соответствующие российские и зарубежные
сертификаты, а также заводскую гарантию качества.
 <P align=justify>
Завод ИНСИ имеет 16 филиалов в России и СНГ.

