<h1>
	Мансардные окна и дополнения к ним.</h1>
<p>
	В случае, когда окна расположены на солнечной стороне, рекомендуется повесить специальные затемняющие шторы, на которые нанесено внешнее светоотражающее покрытие. Затемняющие шторы служат незаменимым аксессуаром в мансардных спальнях, детских и комнатах, где располагаются домашние кинотеатры. Если это просто рабочая зона типа библиотеки или кабинета, можно обойтись установкой жалюзи.</p>
<p>
	Использование жалюзи дает отличную возможность полного контроля объемов и направления солнечных лучей, которые проникают в комнату. Особенность жалюзи заключается в термоотражающем покрытии, расположенном с внешней стороны, что обеспечивает сохранность тепла в холодный период и значительно сокращает его проникновение в дом в жару. Для того, чтобы избежать нагрева комнаты солнечными лучами, жалюзи разворачивают так, чтобы солнце светило на термотражающее покрытие. Если же требуется тепло беречь, то жалюзи поворачивают, наоборот, вовнутрь помещения.</p>
<p>
	Использование такого классического защитного варианта, как шторы, помогает создать особую уютную атмосферу и отрегулировать поступление света в помещение.</p>
<p>
	Одним из наиболее эффективных средств защиты от солнца жарким летом считается маркизет. Благодаря ему на 65% уменьшается количество тепла, попадающего в помещение. Маркизет рассеивает солнечные лучи до того момента, как они попадают на само окно. К тому же стоит отметить, что материал из мелкой сетки никак не влияет на окружающий вид, поскольку остается невидим взгляду. Крепление маркизета осуществляется с внешней части окна, но им вполне легко можно управлять изнутри помещения. Если маркизетом не пользуются, то он сворачивается и заправляется под накладку окна, находящуюся сверху. Устанавливая защиту против солнца можно совместить и маркизет, и жалюзи, и внутреннюю шторку.</p>
<p>
	Если окна располагаются на довольно большой высоте, то в этом случае для управления окнами довольно успешно применяют стержень, шнур и систему дистанционного открывания.</p>
<p>
	На окнах может быть установлена противодождевая и противодымная автоматика. Работа устройства непосредственно связана с центральным процессором противодымной системы. Этот процессор в состоянии получать и обрабатывать сигналы и от других систем управления.</p>
<p>
	Установка мансардных окон происходит либо на обрешетку, либо они крепятся к самой стропильной конструкции. Останавливая свой выбор на том или ином виде монтажа, стоит помнить, что в случае неправильной установки может быть прервана подкровельная вентиляция, что приведет к выпадению конденсата в оконном утеплителе и его намоканию.</p>
<p>
	Устанавливая мансардные окна, прислушивайтесь к советам профессионалов и в точности выполняйте инструкции по монтажу производителей, где есть четкие показания к проведению определенных важных мероприятий, выполнение которых гарантирует качественную и долговременную работу оконных конструкций в течение всего периода эксплуатации. В качестве примера можно рассматривать совет производителей, оставлять между стропилами и оконной коробкой зазор для установки утеплителя. После установки мансардного окна, а также усадки строительной конструкции разработанная фурнитура дает возможность легко регулировать створки окна относительно коробки. Благодаря этому происходит равномерное прижатие уплотнителей из резины без демонтажа окна.</p>



