<h1>Каким должен быть офисный шкаф?</h1>
<p>На этот вопрос невозможно ответить однозначно хотя бы потому, что офисы бывают разными. Речь, конечно же, идет о дизайне. Любая офисная мебель – шкафы, столы или  стулья – выбирается, исходя из особенностей интерьера помещения. Это может быть классический вариант или мебель, реализованная в стиле hi-tech. Все ограничено только вашей фантазией. </p>
<p>Не стоит забывать, что офисный шкаф – вещь функциональная. Поэтому, перед тем, как покупать мебель, желательно определиться с тем, какие задачи она будет решать. Это может быть офисный шкаф для документов, одежды, дисков или образцов продукции. В зависимости от назначения выбирается конструкция, определяется оптимальное количество полок и пр. </p>
<p>Как и другая офисная мебель, шкафы, несомненно, должны быть практичными и долговечными. В отличие от домашней мебели, они испытывают серьезные нагрузки. Следовательно, офисные шкафы для документов обычно рекомендуется выбирать металлические. Впрочем, здесь не существует четких правил. При желании всегда можно найти и деревянный офисный шкаф, который по прочности не уступит изделию из металла. </p>











