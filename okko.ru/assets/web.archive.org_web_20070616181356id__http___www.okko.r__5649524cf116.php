<h2>Мансардные окна</h2>
<p align='justify'>Мансарда очень романтическое место в доме, но и оно требует четкого рационального подхода к организации интерьера и в первую очередь освещения. Ведь любое жилое помещение, будь то спальня или ванная комната требует, определенного уровня освещенности.</p>
